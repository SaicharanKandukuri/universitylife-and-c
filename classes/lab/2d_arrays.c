/* 2d array [row][column] */
/*
    for [3] [3] array
    _______________________________
   | Column  | 0      1       2    |
   |_________|_____________________|
   | Row 0   |                     |
   |_________|_____________________|
   |     1   |                     |
   |_________|_____________________|
   |     2   |                     |
   |_________|_____________________|
*/
#include <stdio.h>

void main(void)
{
    int i,j;
    int twod_a[2][3]={
        {1,2,3},
        {4,5,6}
    };

    for(i=0; i<2;i++)
    {
        for (j=0;j<3;j++) {
            printf("a[%d][%d] = %d \n",i,j,twod_a[i][j]);
        }
    }
}
